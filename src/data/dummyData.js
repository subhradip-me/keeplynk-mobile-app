// Helper functions
export const getFavoriteResources = () => {
  return DUMMY_RESOURCES.filter(resource => resource.isFavorite);
};

export const getResourcesByFolder = (folderId) => {
  return DUMMY_RESOURCES.filter(resource => resource.folderId === folderId);
};

export const getResourcesByTag = (tagName) => {
  return DUMMY_RESOURCES.filter(resource => 
    resource.tags.some(tag => tag.toLowerCase() === tagName.toLowerCase())
  );
};

export const getPopularTags = (limit = 10) => {
  return DUMMY_TAGS
    .sort((a, b) => b.usageCount - a.usageCount)
    .slice(0, limit);
};

export const getFrequentFolders = (limit = 5) => {
  return DUMMY_FOLDERS
    .sort((a, b) => b.itemCount - a.itemCount)
    .slice(0, limit);
};
